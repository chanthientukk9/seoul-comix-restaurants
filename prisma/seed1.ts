async function main() {
  restaurants.forEach(async (restaurant) => {
    const sRestau = await prisma.restaurant.findFirst({
      where: {
        id: restaurant.id
      },
      select: {
        id: true,
        images: {
          select: {
            id: true,
            url: true
          }
        }
      }
    })
    sRestau?.images.forEach(async (image, index) => {
      await prisma.image.update({
        where: {
          id: image.id
        },
        data: {
          url: restaurant.images[index]
        },
      });
    })
  });
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
